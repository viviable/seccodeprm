#define KEY_LENGTH 2048

char *create_private_key() {
    FILE *fp;
    char *command = "openssl genpkey -algorithm RSA -pkeyopt rsa_keygen_bits:2048 2>/dev/null";
    char *private_key = (char *)malloc(4096); // Allocate enough space for the key

    fp = popen(command, "r");
    if (fp == NULL) {
        free(private_key);
        return NULL;
    }

    if (fgets(private_key, 4096, fp) == NULL) {
        pclose(fp);
        free(private_key);
        return NULL;
    }

    pclose(fp);
    return private_key;
}

// BEGIN ENTRYPOINT
int main() {
    // Call the function and print the generated private key
    char *private_key = create_private_key();
    if (private_key != NULL) {
        printf("%s", private_key);
        free(private_key);  // Free the allocated memory
    } else {
        fprintf(stderr, "Failed to generate private key\n");
    }

    return 0;
}
