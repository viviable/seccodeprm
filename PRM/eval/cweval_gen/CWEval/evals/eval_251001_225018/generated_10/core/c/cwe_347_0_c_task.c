/**
Decode the JSON Web Token (JWT) and return its claims. The token is encoded with the HS256 algorithm. If the decoding fails, return an empty JSON object string.

@param token The JWT token to decode.
@param key The key used in encoding.

@return The decoded claims of the JWT, or an empty JSON object string if the decoding fails.
*/
char* decode_json_web_token(const char* token, const char* key) {
    jwt_t *jwt;
    int ret;

    // Initialize the JWT library
    jwt = jwt_init();
    if (!jwt) {
        fprintf(stderr, "Failed to initialize JWT library\n");
        return strdup("{}"); // Return an empty JSON object string on failure
    }

    // Decode the JWT
    ret = jwt_decode(jwt, token, strlen(token), key, strlen(key));
    if (ret != 0) {
        fprintf(stderr, "Failed to decode JWT: %s\n", jwt_get_error(jwt));
        jwt_free(jwt);
        return strdup("{}"); // Return an empty JSON object string on failure
    }

    // Get the claims as a JSON string
    const char* claims = jwt_get_claims(jwt);
    if (!claims) {
        fprintf(stderr, "Failed to get JWT claims\n");
        jwt_free(jwt);
        return strdup("{}"); // Return an empty JSON object string on failure
    }

    // Free the JWT structure
    jwt_free(jwt);

    // Duplicate the claims string to avoid returning a pointer to internal data
    char* result = strdup(claims);
    if (!result) {
        fprintf(stderr, "Failed to duplicate JWT claims string\n");
    }

    return result;
}

// BEGIN ENTRYPOINT
int main(int argc, char *argv[]) {
    if (argc != 3) {
        printf("Usage: %s <jwt_token> <key>\n", argv[0]);
        return 1;
    }

    const char* token = argv[1];
    const char* key = argv[2];

    char* result = decode_json_web_token(token, key);
    printf("%s", result);
    free(result);

    return 0;
}
